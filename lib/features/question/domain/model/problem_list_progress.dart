class ProblemListProgress {
  final int totalEasy;
  final int totalMedium;
  final int totalHard;
  final int solvedEasy;
  final int solvedMedium;
  final int solvedHard;
  final double userBeatsEasy;
  final double userBeatsMedium;
  final double userBeatsHard;

  ProblemListProgress({
    required this.totalEasy,
    required this.totalMedium,
    required this.totalHard,
    required this.solvedEasy,
    required this.solvedMedium,
    required this.solvedHard,
    required this.userBeatsEasy,
    required this.userBeatsMedium,
    required this.userBeatsHard,
  });
}
